-- View otimizada para o Dashboard e acompanhamento de lotes
CREATE OR REPLACE FORCE EDITIONABLE VIEW "VW_RNCH_DEP_RELATORIO_CONSOLIDADO" (
    "NU_RELATORIO", 
    "CO_LOTE_ID", 
    "NO_CURSO", 
    "NO_OPERADOR", 
    "DH_GERACAO", 
    "QTD_APROVADOS", 
    "QTD_BLOQUEADOS", 
    "QTD_TRANSMITIDOS", 
    "DS_TEXTO_RELATORIO"
) AS 
WITH APROVADOS AS ( 
    SELECT CO_LOTE, COUNT(*) AS QTD, MAX(NO_CURSO) AS NO_CURSO  
    FROM TB_RNCH_DEP_AUD_APROVADO  
    GROUP BY CO_LOTE 
), 
BLOQUEADOS AS ( 
    SELECT CO_LOTE, COUNT(*) AS QTD, MAX(NO_CURSO) AS NO_CURSO  
    FROM TB_RNCH_DEP_AUD_BLOQUEADO  
    GROUP BY CO_LOTE 
), 
TRANSMITIDOS AS ( 
    SELECT CO_LOTE, COUNT(*) AS QTD  
    FROM TB_RNCH_DEP_SERPRO  
    WHERE CO_LOTE IS NOT NULL -- So conta os que tem vinculo explicito 
    GROUP BY CO_LOTE 
) 
SELECT  
    L.CO_SEQ_LOG AS NU_RELATORIO, 
    L.CO_LOTE AS CO_LOTE_ID, 
    NVL(A.NO_CURSO, NVL(B.NO_CURSO, 'Não Identificado')) AS NO_CURSO, 
    L.NO_USUARIO AS NO_OPERADOR, 
    L.DH_LOG AS DH_GERACAO, 
    NVL(A.QTD, 0) AS QTD_APROVADOS, 
    NVL(B.QTD, 0) AS QTD_BLOQUEADOS, 
    NVL(T.QTD, 0) AS QTD_TRANSMITIDOS, 
    'Relatório Nº ' || L.CO_SEQ_LOG || ' Gerado pelo operador ' || NVL(L.NO_USUARIO, 'Desconhecido') || CHR(10) || 
    'Em ' || TO_CHAR(L.DH_LOG, 'DD/MM/YYYY HH24:MI') || ' foi gerada a Tabela ETL FINAL com ' ||  
    NVL(A.QTD, 0) || ' APROVADOS e ' || 
    NVL(B.QTD, 0) || ' BLOQUEADOS.' || CHR(10) || 
    'Transmitido ao RENACH Web com ' || NVL(T.QTD, 0) || ' transações retornadas pelo SERPRO.' AS DS_TEXTO_RELATORIO 
FROM TB_RNCH_DEP_AUD_LOG L 
LEFT JOIN APROVADOS A ON A.CO_LOTE = L.CO_LOTE 
LEFT JOIN BLOQUEADOS B ON B.CO_LOTE = L.CO_LOTE 
LEFT JOIN TRANSMITIDOS T ON T.CO_LOTE = L.CO_LOTE 
WHERE L.DS_MENSAGEM LIKE 'Lote%processado%' 
ORDER BY L.CO_SEQ_LOG DESC;


