create or replace PACKAGE BODY PKG_RNCH_DEP AS
    
    PROCEDURE LIMPAR_STAGING IS
    BEGIN
        EXECUTE IMMEDIATE 'TRUNCATE TABLE RNCH_DEP_TB_STG_REL_CURSO';
        EXECUTE IMMEDIATE 'TRUNCATE TABLE RNCH_DEP_TB_STG_FORM_CAD';
        EXECUTE IMMEDIATE 'TRUNCATE TABLE RNCH_DEP_TB_STG_SERPRO'; 
    END LIMPAR_STAGING;

    PROCEDURE PROCESSAR_SERPRO(p_no_curso IN VARCHAR2) IS
    BEGIN
        INSERT INTO RNCH_DEP_TB_SERPRO (NU_TRANSACAO, NU_CPF, NO_CURSO, DS_MENSAGEM, DH_PROCESSAMENTO)
        SELECT 
            S.COL001, 
            LPAD(REGEXP_REPLACE(S.COL002, '[^0-9]', ''), 11, '0'), 
            p_no_curso,
            S.COL003, 
            S.COL004
        FROM RNCH_DEP_TB_STG_SERPRO S
        WHERE S.COL002 IS NOT NULL
          AND NOT EXISTS (
              SELECT 1 FROM RNCH_DEP_TB_SERPRO T
              WHERE T.NU_CPF           = LPAD(REGEXP_REPLACE(S.COL002, '[^0-9]', ''), 11, '0')
                AND T.DS_MENSAGEM      = S.COL003
                AND T.DH_PROCESSAMENTO = S.COL004
                AND T.NO_CURSO         = p_no_curso
          );
        
        DELETE FROM RNCH_DEP_TB_STG_SERPRO;
        
        COMMIT;
    END PROCESSAR_SERPRO;

    PROCEDURE PROCESSAR_ARQUIVOS (
        p_seq_inicio        IN NUMBER,
        p_tipo_atualizacao  IN VARCHAR2,
        p_carga_horaria     IN VARCHAR2,
        p_co_curso          IN VARCHAR2 DEFAULT '04',
        p_lote_id           OUT NUMBER
    ) IS
        v_lote_id         NUMBER;
        v_hoje_juliano    VARCHAR2(3);
        v_user_safe       VARCHAR2(100); 
        v_qtd_registros   NUMBER; 
        v_log_mensagem    VARCHAR2(4000); 
    BEGIN
        v_user_safe := NVL(V('APP_USER'), USER);
        v_lote_id := TO_NUMBER(TO_CHAR(SYSDATE, 'YYYYMMDDHH24MI'));
        p_lote_id := v_lote_id;
        v_hoje_juliano := LPAD(TO_CHAR(SYSDATE, 'DDD'), 3, '0');

        DELETE FROM RNCH_DEP_TB_ETL_FINAL;

        -- 1. BLOQUEIO
        INSERT INTO RNCH_DEP_TB_AUD_BLOQUEADO (
            NU_CPF, DS_MOTIVO_BLOQUEIO, DT_MATRICULA, DT_CONCLUSAO, 
            NO_CURSO, NO_ORGAO, TP_SEXO, SG_UF_ALUNO, NO_MUNICIPIO_ALUNO, NU_NOTA,
            CO_LOTE, DH_AUDITORIA
        )
        SELECT 
            REGEXP_REPLACE(NU_CPF, '[^0-9]', ''), 
            'Duração inferior a 5 dias',
            TO_DATE(DT_MATRICULA, 'DD/MM/YYYY'),
            TO_DATE(DT_CONCLUSAO, 'DD/MM/YYYY'),
            NO_CURSO, NO_ORGAO, 
            CASE WHEN UPPER(TP_SEXO) LIKE 'M%' THEN 'M' ELSE 'F' END, 
            SG_UF_ALUNO, NO_MUNICIPIO_ALUNO, 
            TO_NUMBER(REPLACE(NU_NOTA, '.', ',')), 
            p_lote_id,
            SYSTIMESTAMP
        FROM RNCH_DEP_TB_STG_REL_CURSO S
        WHERE (TO_DATE(DT_CONCLUSAO, 'DD/MM/YYYY') - TO_DATE(DT_MATRICULA, 'DD/MM/YYYY')) < 5
          AND NOT EXISTS (
              SELECT 1 FROM RNCH_DEP_TB_AUD_BLOQUEADO B
              WHERE B.NU_CPF         = REGEXP_REPLACE(S.NU_CPF, '[^0-9]', '')
                AND B.NO_CURSO       = NVL(S.NO_CURSO, 'Não Informado')
                AND B.DT_CONCLUSAO   = TO_DATE(S.DT_CONCLUSAO, 'DD/MM/YYYY')
          );

        -- 2. ETL PRINCIPAL
        INSERT INTO RNCH_DEP_TB_ETL_FINAL (
            NU_SEQ_TRANS, CO_TRANS, CO_MOD_TRANS, NU_CPF_USUARIO, SG_UF_OR_TRANS,
            SG_UF_ORIG_TRANSM, SG_UF_DES_TRANSM, CO_COND_TRANS, NU_TAM_TRANS, CO_RET_TRANS,
            NU_DIA_JULIANO, TP_CHAVE, NU_CNH, TP_EVENTO, TP_ATUALIZACAO,
            CO_CURSO, CO_MODALIDADE, NU_CERTIFICADO, DT_INICIO_CURSO,
            DT_FIM_CURSO, NU_CARGA_HORARIA, NU_CNPJ_ENTIDADE, NU_CPF_INSTRUTOR,
            CO_MUNICIPIO_CURSO, SG_UF_CURSO, DT_VALIDADE, SG_CATEGORIA, DS_OBSERVACOES, CO_LOTE, CO_SEQ_FONTE
        )
        SELECT 
            LPAD(p_seq_inicio + ROW_NUMBER() OVER (ORDER BY r.NO_ALUNO) - 1, 6, '0'),
            '181', '7',
            LPAD(REGEXP_REPLACE(r.NU_CPF, '[^0-9]', ''), 11, '0'), 
            'SA', 'SA', 'BR', '0', '0152', '00', 
            v_hoje_juliano, '2',
            LPAD(REGEXP_REPLACE(f.NU_CNH, '[^0-9]', ''), 11, '0'),
            'C', 
            RPAD(UPPER(p_tipo_atualizacao), 1, ' '),
            RPAD(p_co_curso, 2, ' '), '2', 
            RPAD('escola' || LPAD(ROW_NUMBER() OVER (ORDER BY r.NO_ALUNO), 9, '0'), 15, ' '),
            TO_CHAR(TO_DATE(r.DT_MATRICULA, 'DD/MM/YYYY'), 'YYYYMMDD'),
            TO_CHAR(TO_DATE(r.DT_CONCLUSAO, 'DD/MM/YYYY'), 'YYYYMMDD'),
            LPAD(p_carga_horaria, 3, '0'),
            '00394494000560', '57437670097', 
            '09701', 
            'DF',
            TO_CHAR(ADD_MONTHS(TO_DATE(r.DT_MATRICULA, 'DD/MM/YYYY'), 60), 'YYYYMMDD'),
            RPAD(f.SG_CATEGORIA_CNH, 4, ' '), 
            RPAD('99', 20, ' '),
            v_lote_id,
            r.CO_SEQ_STG_REL_CURSO 
        FROM RNCH_DEP_TB_STG_REL_CURSO r
        JOIN RNCH_DEP_TB_STG_FORM_CAD f 
            ON REGEXP_REPLACE(r.NU_CPF, '[^0-9]', '') = REGEXP_REPLACE(f.NU_CPF, '[^0-9]', '')
        WHERE (TO_DATE(r.DT_CONCLUSAO, 'DD/MM/YYYY') - TO_DATE(r.DT_MATRICULA, 'DD/MM/YYYY')) >= 5;

        v_qtd_registros := SQL%ROWCOUNT;

        -- 3. AUDITORIA APROVADOS
        INSERT INTO RNCH_DEP_TB_AUD_APROVADO (
            NU_CPF, DT_FIM_CURSO, 
            NO_CURSO, NO_ORGAO, TP_SEXO, SG_UF_ALUNO, NO_MUNICIPIO_ALUNO, NU_NOTA,
            CO_LOTE, DH_AUDITORIA
        )
        SELECT 
            f.NU_CPF_USUARIO, TO_DATE(f.DT_FIM_CURSO, 'YYYYMMDD'), 
            s.NO_CURSO, s.NO_ORGAO, 
            CASE WHEN UPPER(s.TP_SEXO) LIKE 'M%' THEN 'M' ELSE 'F' END,
            s.SG_UF_ALUNO, s.NO_MUNICIPIO_ALUNO, 
            TO_NUMBER(REPLACE(s.NU_NOTA, '.', ',')),
            v_lote_id, SYSTIMESTAMP
        FROM RNCH_DEP_TB_ETL_FINAL f
        JOIN RNCH_DEP_TB_STG_REL_CURSO s ON s.CO_SEQ_STG_REL_CURSO = f.CO_SEQ_FONTE 
        WHERE f.CO_LOTE = v_lote_id
          AND NOT EXISTS (
              SELECT 1 FROM RNCH_DEP_TB_AUD_APROVADO A
              WHERE A.NU_CPF       = f.NU_CPF_USUARIO
                AND A.NO_CURSO     = s.NO_CURSO
                AND A.DT_FIM_CURSO = TO_DATE(f.DT_FIM_CURSO, 'YYYYMMDD')
          );

        -- 4. LOG
        v_log_mensagem := 'Lote ' || v_lote_id || ' processado com sucesso. Registros gerados: ' || v_qtd_registros;
        
        INSERT INTO RNCH_DEP_TB_AUD_LOG (DS_MENSAGEM, NO_USUARIO) 
        VALUES (v_log_mensagem, v_user_safe);
        
        -- 5. LIMPEZA
        LIMPAR_STAGING;
        COMMIT;
        
    EXCEPTION
        WHEN OTHERS THEN
            ROLLBACK;
            v_log_mensagem := 'Erro FATAL no processamento: ' || SQLERRM;
            INSERT INTO RNCH_DEP_TB_AUD_LOG (DS_MENSAGEM, NO_USUARIO) 
            VALUES (v_log_mensagem, v_user_safe);
            RAISE; 

    END PROCESSAR_ARQUIVOS;

END PKG_RNCH_DEP;
